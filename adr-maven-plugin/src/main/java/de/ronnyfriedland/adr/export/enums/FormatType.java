package de.ronnyfriedland.adr.export.enums;

/**
 * Defines the available (export-) formats.
 *
 * @author ronnyfriedland
 */
public enum FormatType {
    /**
     * Export adrs as docx (office document)
     */
    docx, 
    /**
     * Export adrs as html
     */
    html, 
    /**
     * Export adrs as pds
     */
    pdf;
}
